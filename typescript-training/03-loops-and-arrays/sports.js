var sports = ["Soccer", "Basketball", "Tennis"];
// simplified for Loop
for (var _i = 0, sports_1 = sports; _i < sports_1.length; _i++) {
    var tempSport = sports_1[_i];
    if (tempSport === "Basketball") {
        console.log(tempSport + " Is my favorite sport");
    }
    else {
        console.log(tempSport + " is not my favorite sport");
    }
}
